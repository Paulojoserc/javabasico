package javabasico.aula36;

public class Telefone {
private String tipo;
private  String ddd;
private int numero;

public Telefone() {
}

public String getTipo() {
	return tipo;
}

public void setTipo(String tipo) {
	this.tipo = tipo;
}

public String getDdd() {
	return ddd;
}

public void setDdd(String ddd) {
	this.ddd = ddd;
}

public int getNumero() {
	return numero;
}

public void setNumero(int numero) {
	this.numero = numero;
}


}
